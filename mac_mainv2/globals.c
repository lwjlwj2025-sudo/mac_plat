// 计算并打印综合性能报告
void Print_Performance_Report(float simulation_time_sec) {
    
    if (simulation_time_sec <= 0) return;

    int active_users = 0; // 在此处定义变量
    double fairness_index = Calculate_Jains_Fairness(&active_users); // 传入变量地址
    printf("\n================ [MAC Layer Performance Report @ %.2fs] ================\n", simulation_time_sec);
    
    // 1. 接入成功率
    float access_rate = (g_MacStats.rach_attempts > 0) ? 
        (float)g_MacStats.rach_success / g_MacStats.rach_attempts * 100.0 : 0.0;
    printf("[Access] Attempts: %d, Success: %d, Rate: %.2f%%\n", 
           g_MacStats.rach_attempts, g_MacStats.rach_success, access_rate);

    // 2. 系统吞吐量 (Mbps)
    // Bytes * 8 / 1e6 / time
    double total_tput = (double)g_MacStats.total_throughput_bytes * 8.0 / 1e6 / simulation_time_sec;
    double rt_tput = (double)g_MacStats.rt_throughput_bytes * 8.0 / 1e6 / simulation_time_sec;
    printf("[Throughput] Total: %.2f Mbps (RT: %.2f Mbps, nRT: %.2f Mbps)\n", 
           total_tput, rt_tput, total_tput - rt_tput);

    // 3. 资源利用率
    float rb_usage = (g_MacStats.total_rb_available > 0) ? 
        (float)g_MacStats.total_rb_used / g_MacStats.total_rb_available * 100.0 : 0.0;
    printf("[Resource] RB Usage: %.2f%% (%d/%d)\n", 
           rb_usage, g_MacStats.total_rb_used, g_MacStats.total_rb_available);

    // 4. 平均时延
    float avg_delay = (g_MacStats.total_packets_sent > 0) ? 
        (float)g_MacStats.total_packet_delay / g_MacStats.total_packets_sent : 0.0;
    printf("[Latency] Avg Packet Delay: %.2f ms\n", avg_delay);

    // 5. 公平性 (Jain's Fairness Index)
    // J = (Σx)^2 / (n * Σx^2)
    // 注意：这里需要遍历所有活跃用户实时计算，或者使用近似统计
    // 我们可以简单打印当前的统计值，具体计算建议遍历 UserInfo_Block
    printf("======================================================================\n");
    printf("[Fairness] Jain's Index: %.4f (Active Users: %d)\n", Calculate_Jains_Fairness(), active_users);
}
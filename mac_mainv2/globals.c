// 打印当前时间
void printCurrentTime()
{
    struct timeval currentTime;
    gettimeofday(&currentTime, NULL);

    struct tm *localTime = localtime(&currentTime.tv_sec);

    int hour = localTime->tm_hour;                // 小时
    int minute = localTime->tm_min;               // 分钟
    int second = localTime->tm_sec;               // 秒数
    int millisecond = currentTime.tv_usec / 1000; // 毫秒
    int microsecond = currentTime.tv_usec % 1000; // 微秒

    LOG("Current Time: %02d:%02d:%02d:%03dms:%03dus\n", hour, minute, second, millisecond, microsecond);
}

